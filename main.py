# Lower and Upper Case Methods in Python
message = "hello world"
print(message.upper())
print(message[0:5].upper())

# Count methods in python
userName = "Paul Hudson"
print(userName.count("u"))

# find method in python
surName = "Vladmir Putin"
print(surName.find("Putin"))

# replace method in python
message = "hello world"

message = message.replace("world", "American")

print(message)